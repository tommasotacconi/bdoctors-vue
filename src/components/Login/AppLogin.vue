<script>
import axios from 'axios';
import confAxios from '../../axios';

export default {
	data() {
		return {
			inputEmail: '',
			inputPassword: '',
			responseStatus: false,
			isAnimationActive: false,
		}
	},
	methods: {
		sendLoginData() {
			// Single page application authentication request (confAxios is axios as configured in axios.js)
			confAxios.get('sanctum/csrf-cookie')
			.then(response => {
				axios.post('http://127.0.0.1:8000/api/login', {
				email: this.inputEmail,
				password: this.inputPassword
				})
				.then(response => {
					console.log(response);
					this.responseStatus = true;
					// Redirect to current userId profile info 
					this.$router.push({ name: 'dashboard', params: { id: response.data.data.id } });
					// Set current loggedUserId in store
					this.store.loggedUserId = response.data.user.id;
				})
				.catch(error => {
					// Trigger animation
					this.isAnimationActive = true;
					setTimeout(() => {this.isAnimationActive = false}, 500);
					console.log(error);
				})
			})
			.catch(error => {
				console.log(error)
			})
		}
	},
}
</script>

<template>
	<form class="" action="post" @submit.prevent="sendLoginData">
		<div class="card login-card row" id="login-card">
			<div class="col-12">
				<!-- Email input -->
				<label for="email-input" class="badge rounded-pill">Email</label>
				<input type="text" id="email-input" class="form-control mb-3" v-model="inputEmail">
			</div>
			<div class="col-12">
				<!-- Password input -->
				<label for="password-input" class="badge rounded-pill">Password</label>
				<input type="password" id="password-input" class="form-control mb-3" v-model="inputPassword">
			</div>
			<!-- Button wrappers -->
			<div class="buttons-wrapper col-12 d-flex justify-content-center">
				<button type="submit" id="login-button" class="btn btn-primary mt-4 mb-3" :class="{ ['shaking-animation']: isAnimationActive }">Login</button>
				<div class="mt-3" v-if="responseStatus">Accesso effettuato</div>
			</div>
		</div>
	</form>
</template>

